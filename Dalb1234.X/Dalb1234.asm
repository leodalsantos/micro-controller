    LIST    P=18F4520
    #include<P18F4520.INC>
    
    ;configuration Bits
    CONFIG OSC=INTIO67,PWRT=ON, WDT=OFF, MCLRE=OFF, DEBUG=OFF
    
REG0	EQU	0x06
REG1	EQU	0x07
REG2	EQU	0x08
CONT	EQU	0x09
	
	org	0x0000
	GOTO	INICIO
	
	org	0x0100
DELAY:
	MOVLW	.10
	MOVWF	REG1
DL1:	MOVLW	0xFF
	MOVWF	REG0
DL0:	DECFSZ	REG0
	BRA	DL0
	DECFSZ	REG1
	BRA	DL1
	RETURN
	
	org	0x1000
INICIO:
	MOVLW	0x78
	MOVWF	OSCCON
	MOVLW	0x0F
	MOVWF	ADCON1
	MOVLW	0x00
	MOVWF	TRISD
	MOVLW	0xC3
	MOVWF	TRISA
	MOVLW	0xC3
	MOVWF	LATA
	MOVLW	.60
	MOVWF	REG2

REPETE_:
	MOVFF	REG2,CONT
REPETE1:
	MOVLW	0x06
	MOVWF	LATD
	BSF	LATA,2
	CALL	DELAY
	BCF	LATA,2
	MOVLW	0X5B
	MOVWF	LATD
	BSF	LATA,3
	CALL	DELAY
	BCF	LATA,3
	MOVLW	0x4F
	MOVWF	LATD
	BSF	LATA,4
	CALL	DELAY
	BCF	LATA,4
	MOVLW	0x66
	MOVWF	LATD
	BSF	LATA,5
	CALL	DELAY
	BCF	LATA,5
	DECFSZ	CONT
	BRA	REPETE1
	
	
	MOVFF	REG2,CONT
REPETE2:
	MOVLW	0x5B
	MOVWF	LATD
	BSF	LATA,2
	CALL DELAY
	BCF	LATA,2
	MOVLW	0X4F
	MOVWF	LATD
	BSF	LATA,3
	CALL DELAY
	BCF	LATA,3
	MOVLW	0x66
	MOVWF	LATD
	BSF	LATA,4
	CALL	DELAY
	BCF	LATA,4
	MOVLW	0x06
	MOVWF	LATD
	BSF	LATA,5
	CALL	DELAY
	BCF	LATA,5
	DECFSZ	CONT
	BRA	REPETE2
	
	MOVFF	REG2,CONT
REPETE3:
	MOVLW	0x4F
	MOVWF	LATD
	BSF	LATA,2
	CALL DELAY
	BCF	LATA,2
	MOVLW	0X66
	MOVWF	LATD
	BSF	LATA,3
	CALL DELAY
	BCF	LATA,3
	MOVLW	0x06
	MOVWF	LATD
	BSF	LATA,4
	CALL	DELAY
	BCF	LATA,4
	MOVLW	0x5B
	MOVWF	LATD
	BSF	LATA,5
	CALL	DELAY
	BCF	LATA,5
	DECFSZ	CONT
	BRA	REPETE3
	
	MOVFF	REG2,CONT
REPETE4:
	MOVLW	0x66
	MOVWF	LATD
	BSF	LATA,2
	CALL DELAY
	BCF	LATA,2
	MOVLW	0X06
	MOVWF	LATD
	BSF	LATA,3
	CALL DELAY
	BCF	LATA,3
	MOVLW	0x5B
	MOVWF	LATD
	BSF	LATA,4
	CALL	DELAY
	BCF	LATA,4
	MOVLW	0x4F
	MOVWF	LATD
	BSF	LATA,5
	CALL	DELAY
	BCF	LATA,5
	DECFSZ	CONT
	BRA	REPETE4
	
	BRA	REPETE_
	
	END


